package javaBasic;

public class variableBasic {

	public static void main(String[] args) {
		// Boolean = 1 Bit
		boolean wahr_oder_falsch;
		wahr_oder_falsch = true;
				System.out.println(wahr_oder_falsch);
		wahr_oder_falsch = false;
				System.out.println(wahr_oder_falsch);
		// Byte
				byte Kurzzahl = 1;
				System.out.println(Kurzzahl);
				System.out.println(Byte.MAX_VALUE);
				System.out.println(Byte.MIN_VALUE);
		//byte = 2bit
				short Zahl = 12;
				System.out.println(Zahl);
				System.out.println(Short.MAX_VALUE);
				System.out.println(Short.MIN_VALUE);
		// int - 4 byte
				int Zahle = 123456;
				System.out.println(Zahle);
				System.out.println(Integer.MAX_VALUE);
				System.out.println(Integer.MIN_VALUE);
		// long - 8 byte
				long Groﬂe_Zahl = 456;
				System.out.println(Groﬂe_Zahl);
				System.out.println(Long.MAX_VALUE);
				System.out.println(Long.MIN_VALUE);
		// float
				float Komma_zahl = 12.34F;
				System.out.println(Komma_zahl);
				System.out.println(Float.MAX_VALUE);
				System.out.println(Float.MIN_VALUE);
		// Double
				Double Groﬂ_Komma_zahl = 124435434.3253532534;
				System.out.println(Groﬂ_Komma_zahl);
				System.out.println(Double.MAX_VALUE);
				System.out.println(Double.MIN_VALUE);
		// char 
				char zeichen = 'a';
				System.out.println(zeichen);
		// String
				String zeichenen = "abcdefghiklmn";
				System.out.println(zeichenen);
	}

}
